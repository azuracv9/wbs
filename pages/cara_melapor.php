<li>Klik tombol "Login", lalu isikan Username dan Password Anda.</li>
<li>Jika Anda belum terdaftar, maka klik tombol "Register" dan isikan data diri Anda lalu klik tombol "Register", maka Anda akan otomatis login ke Aplikasi.</li>
        <ul>
		<li>Buat <b>Nama Samaran (username)</b> dan <b>Kata Sandi (password)</b> yang anda ketahui sendiri</li>
        <li>Gunakan nama yang unik dan tidak menggambarkan identitas anda</li>
		</ul>
<li>Klik menu "Pengaduan" untuk merekam pengaduan baru.</li>
<li>Klik tombol "Tambah Pengaduan" untuk menambahkan pengaduan baru</li>
<li>Isi form Tambah Pengaduan sesuai informasi yang anda ketahui, lalu klik tombol "Lanjut"</li>
<li>Perhatikan baik-baik beberapa hal di bawah ini:</li>
        <ul>
		<li>Semua kotak yang diberi tanda (*) wajib diisi.</li>
        <li>Pastikan informasi yang diberikan sedapat mungkin memenuhi unsur 4W + 1H</li>
		</ul>
<li>Jika anda memiliki bukti dalam bentuk file seperti foto atau dokumen lain, silahkan dilengkapi di halaman pengaduan. Caranya:</li>
        <ul>
		<li>Setelah membaca petunjuk untuk menyertakan lampiran, klik kotak kecil di bawah petunjuk tersebut, dan lanjutkan prosesnya.</li>
		</ul>
<li>Setelah selesai mengisi, silahkan klik tombol "Kirim" untuk melanjutkan atau klik tombol "Hapus" untuk membatalkan proses pelaporan anda.</li>
<li>Halaman berikutnya memberi kesempatan bagi anda yang ingin mencetak nomor register pengaduan.</li>
        <ul>
		<li><b>Catat</b> dan <b>simpan</b> dengan baik <b>Nama Samaran (username)</b> dan <b>Kata Sandi (password).</b></li>
        <li>Simpan dengan baik nomor register yang Anda peroleh saat melakukan pengaduan untuk mengetahui status/
        tindak lanjut pengaduan yang Anda sampaikan.</li>
        <li>Kementerian Pertanian akan menghubungi Anda melalui saluran yang telah Anda cantumkan dalam form pengaduan apabila pengaduan yang Anda sampaikan belum memenuhi kriteria untuk ditindaklanjuti.</li>
</ol>
